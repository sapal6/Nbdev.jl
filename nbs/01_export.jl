function collect_nuclei(notebook::Notebook, notebook_cells)
	cells=Cell[]
    for i in 1:length(notebook_cells)
		raw_code=code(notebook_cells[i])
		
		if !startswith(raw_code, "#hide") && !startswith(raw_code, "#export") 
	        push!(cells,Cell(raw_code))
        end
		
	end
	cells
end
end

