function _load_notebook(filename::String, marker::String)
		notebook=load_notebook(filename)
		collected_nuclei=nuclei(collect_nuclei(notebook, marker))
		
		if marker=="md"
			Notebook(collected_nuclei)
		else
			Nb(collected_nuclei, filename)
		end
end

end

